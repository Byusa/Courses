# Continue statement
# we stop the current iteration and continue the iteration
i = 0
while i < 6:
  i += 1
  if i == 3:
    continue
  print(i)


# Else Statement 
# Else can run when the condition no longer true
i = 1
while i < 6:
  print(i)
  i += 1
else:
  print("i is no longer less than 6")





